export function generateGreekName() {
  const isMale = Math.random() > 0.5;
  if (isMale) {
    const name = GREEK_MALE_NAMES[Math.floor(Math.random() * GREEK_MALE_NAMES.length)];
    const surname = GREEK_SURNAMES_MALE[Math.floor(Math.random() * GREEK_SURNAMES_MALE.length)];
    return `${name} ${surname}`;
  } else {
    const name = GREEK_FEMALE_NAMES[Math.floor(Math.random() * GREEK_FEMALE_NAMES.length)];
    const surname = GREEK_SURNAMES_FEMALE[Math.floor(Math.random() * GREEK_SURNAMES_FEMALE.length)];
    return `${name} ${surname}`;
  }
}
